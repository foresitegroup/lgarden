  In 2003 my mother, an avid gardener had double knee surgery at 76 years old. While my sisters helped her with rehabbing, my mission was to explore gardening products she would be able to use that summer. My occupation was sales and marketing of consumer products in lawn and garden. I quickly realized there weren't any products designed specifically for the gardener who had limited mobility. It was at that point with the assistance of a design engineer, we created some outstanding conceptual drawings. Jack Stein, a legend in lawn and garden retailing, loved the concept but cautioned that designing a "model T before the Cadillac" would be the prudent path. For six years the designs served as mere decorations on my office wall. Thanks to the enthusiasm of a younger generation promoting the idea of sustainable living, <em>refusing to believe you can't garden if you don't have nutrient rich soil under your feet</em> &mdash; we updated the designs and launched this cross generational product offering.<br>
